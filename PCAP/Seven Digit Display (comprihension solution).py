pattern ={
"1": ['  #', '  #', '  #', '  #', '  #'],
"2": ['###', '  #', '###', '#  ', '###'],
"3": ['###', '  #', '###', '  #', '###'],
"4": ['# #', '# #', '###', '  #', '  #'],
"5": ['###', '#  ', '###', '  #', '###'],
"6": ['###', '#  ', '###', '# #', '###'],
"7": ['###', '  #', '  #', '  #', '  #'],
"8": ['###', '# #', '###', '# #', '###'],
"9": ['###', '# #', '###', '  #', '###'],
"0": ['###', '# #', '# #', '# #', '###'],
".": ['   ', '   ', '   ', '   ', '  #']}       # dictionary containing patter for each digit

def ssd(n):
    digits = [pattern[digit] for digit in n]
    for i in range(5):
        print("  ".join(segment[i] for segment in digits))


num = input("Enter a digit : ")
ssd(num)